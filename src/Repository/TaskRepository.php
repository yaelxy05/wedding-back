    public function findTaskForOneUser(User $user)
    {
        $user = $user->getId();
        $entityManager = $this->getEntityManager();

        $query = $entityManager->createQuery(
            'SELECT a
            FROM App\Entity\Task a
            WHERE a.user = ' . $user
        );

        return $query->getResult();
    }

